'use server';

import { generateText } from 'ai';
import { createOpenRouter } from '@openrouter/ai-sdk-provider';

export interface ReviewIssue {
  level: 'error' | 'warning' | 'info';
  section: string;
  issue: string;
  suggestion?: string;
}

export interface DocumentReview {
  isValid: boolean;
  issues: ReviewIssue[];
  summary: string;
}

/**
 * Review agent — проверяет готовый документ (протокол) на ошибки:
 * - Структура и полнота
 * - Логичность и связность
 * - Пунктуация и орфография
 * - Посторонние символы (китайские, невидимые и т.д.)
 * - Консистентность терминологии
 * 
 * ВАЖНО: Проверка детерминированная — одинаковый документ всегда выдаёт одинаковые замечания.
 */
export async function runDocumentReview(documentMarkdown: string): Promise<DocumentReview> {
  // Нормализация документа перед проверкой
  const normalizedDoc = documentMarkdown
    .replace(/\r\n/g, '\n')
    .trim();

  const reviewPrompt = `# Вы — эксперт по проверке деловых документов (протоколов обследования).
Ваша задача — найти ВСЕ ошибки в документе и вывести их в СТРОГО определённом формате.

## ДОКУМЕНТ ДЛЯ ПРОВЕРКИ:
\`\`\`
${normalizedDoc}
\`\`\`

================================================================================
## ДЕТЕРМИНИРОВАННЫЕ ПРАВИЛА ПРОВЕРКИ (применять ВСЕГДА одинаково):
================================================================================

ОБЯЗАТЕЛЬНЫЕ РАЗДЕЛЫ (должны быть ВСЕ 10):
1. Дата встречи (формат: ДД.ММ.ГГГГ)
2. Повестка (тема + пункты)
3. Участники (таблицы: Заказчик и Исполнитель)
4. Термины и определения
5. Сокращения и обозначения
6. Содержание встречи
7. Вопросы и ответы
8. Решения с ответственными
9. Открытые вопросы
10. Согласовано (подписи)

================================================================================
## ТИПЫ ОШИБОК (строго классифицировать):
================================================================================

ERROR (критическая ошибка — требует обязательного исправления):
- Отсутствует обязательный раздел (из 10 выше) — ПРОВЕРЯЙТЕ ВНИМАТЕЛЬНО, не пропускайте существующие разделы
- Раздел пуст или содержит "Информация не предоставлена", "N/A", "незаполнено"
- Найден китайский/японский символ (CJK Unicode: \\u3000-\\u30FF, \\u3400-\\u4DBF, \\u4E00-\\u9FFF) — ПРОВЕРЯЙТЕ ТОЛЬКО явные символы, не обычные пробелы
- В разделе "Решения" отсутствует ответственный
- В разделе "Участники" отсутствует ФИО **или должность** в таблице
- **ФИО участника состоит только из имени без фамилии** (например, "Екатерина" без фамилии) — это ERROR
- Дата встречи не в формате ДД.ММ.ГГГГ
- **Найдены слова/фразы не делового стиля** (например, "Мяу", "привет", "пока", "классно", сленг, эмоциональные выражения, разговорные слова) — это ERROR

WARNING (предупреждение — желательно исправить):
- Опечатка в слове (проверка по контексту)
- Неправильная пунктуация (отсутствует запятая, точка в конце предложения)
- Несоответствие терминологии (один термин назван по-разному в разных разделах)
- Таблица имеет неверный формат (не выровнены колонки, отсутствует заголовок)
- Нумерация пунктов нарушена (пропущен номер, дублируется)
- Стилистическая ошибка (канцеляризм, плеоназм)
- **В ФИО только имя и фамилия без отчества** (для делового документа желательно полное ФИО)
- Раздел содержит мало данных (например, менее 3 терминов в разделе "Термины")
- **Подозрительные фразы** (неформальные выражения, разговорный стиль, избыточная эмоциональность)

INFO (информация — не требует исправления):
- Документ соответствует всем требованиям
- Разделы заполнены полностью
- Терминология консистентна
- Форматирование корректно

================================================================================
## АЛГОРИТМ ПРОВЕРКИ (выполнять ПОШАГОВО):
================================================================================

### ШАГ 1: Проверка структуры (ВНИМАТЕЛЬНО)
  - Прочитай документ ПОЛНОСТЬЮ от начала до конца
  - Найди все 10 разделов по заголовкам с цифрами (1., 2., 3. ... 10.)
  - Запиши в reasoning КАЖДЫЙ найденный раздел с его заголовком
  - Сравни список с эталонным: [Дата, Повестка, Участники, Термины, Сокращения, Содержание, Вопросы, Решения, Открытые вопросы, Согласовано]
  - Если раздел НАЙДЕН — НЕ создавай ошибку
  - ERROR ТОЛЬКО если раздел ДЕЙСТВИТЕЛЬНО отсутствует (нет заголовка с цифрой)

### ШАГ 2: Проверка полноты
  - Каждый раздел содержит данные (не пустой)? → Если пусто: ERROR
  - Таблицы имеют строки с данными? → Если таблица пустая (только заголовок): ERROR

### ШАГ 3: Проверка кодировки (ТОЛЬКО явные CJK символы)
  - Ищи символы в диапазонах: \\u3000-\\u30FF (CJK), \\u3400-\\u4DBF, \\u4E00-\\u9FFF
  - НЕ помечай обычные пробелы (\\u0020) или неразрывные пробелы (\\u00A0)
  - НЕ помечай символы кириллицы (\\u0400-\\u04FF)
  - НЕ помечай латиницу (\\u0000-\\u007F)
  - Если найден CJK символ — укажи его точно в reasoning: символ, Unicode, позиция
  - Затем создай ERROR

### ШАГ 4: Проверка формата дат
  - Дата в формате ДД.ММ.ГГГГ? → Если нет: ERROR

### ШАГ 5: Проверка таблиц
  - Таблицы имеют заголовки колонок (| ФИО | Должность |)?
  - Все строки заполнены данными (нет пустых ячеек)?

### ШАГ 6: ПРОВЕРКА ФИО (КРИТИЧЕСКИ ВАЖНО)
  - Для КАЖДОГО участника в разделе 3 (Участники) проверь ФИО:
  - **ФИО должно содержать ФАМИЛИЮ и ИМЯ** (минимум 2 слова)
  - Если ФИО состоит ТОЛЬКО из имени (например, "Екатерина", "Иван") — это ERROR
  - Если ФИО состоит из имени и фамилии, но без отчества — это WARNING (неполное ФИО)
  - Если ФИО содержит 3 слова (фамилия, имя, отчество) — это корректно
  - В reasoning запиши проверку каждого участника: "ФИО [полное/неполное/только имя]"

### ШАГ 7: ПРОВЕРКА СТИЛЯ (КРИТИЧЕСКИ ВАЖНО для делового документа)
  - Проверь КАЖДЫЙ раздел на наличие слов не делового стиля:
  - **Эмоциональные выражения**: восклицательные знаки в определениях, разговорные частицы
  - **Сленг**: любые неформальные выражения, молодёжный сленг, интернет-мемы
  - **Разговорный стиль**: фразы, уместные в личной переписке, но не в деловом документе
  - Если найдено слово/фраза не делового стиля — это WARNING
  - В reasoning запиши: "Стиль: [деловой/неформальный]", найденные слова

### ШАГ 8: Проверка терминологии
  - Один термин используется одинаково во всём документе? → Если нет: WARNING

### ШАГ 9: Проверка пунктуации
  - Точки в конце предложений?
  - Запятые в перечислениях?

================================================================================
## ВАЖНЫЕ ПРАВИЛА (НЕ НАРУШАТЬ):
================================================================================

1. НЕ выдумывай ошибки — проверяй ФАКТИЧЕСКИЕ данные в документе
2. НЕ помечай существующие разделы как отсутствующие
3. НЕ помечай обычные пробелы как китайские символы
4. Если раздел НАЙДЕН — запиши его и НЕ создавай ошибку
5. Если сомневаешься — лучше пропусти, чем создать ложную ошибку
6. ПЕРЕПРОВЕРЬ каждую ошибку перед включением в ответ

================================================================================
## ФОРМАТ ОТВЕТА (строго следуй инструкции):
================================================================================

Сначала напиши **ПОДРОБНЫЕ РАССУЖДЕНИЯ** (reasoning) в свободном формате:
- Перечисли все 10 разделов и отметь, найден каждый или нет (например: "Раздел 1: найден, Дата встречи: 07.04.2026")
- Для каждого найденного раздела кратко опиши содержание
- **Обязательно проверь ФИО всех участников** — запиши в reasoning: "Участник [ФИО]: полное/неполное/только имя"
- **Обязательно проверь стиль документа** — запиши: "Стиль: деловой/неформальный", найденные слова-маркеры ("Мяу", сленг и т.д.)
- Если нашёл символы CJK — укажи точно, где и какие
- Объясни, почему каждая ошибка — это ошибка

Затем напиши **ТОЛЬКО JSON** в формате:

{
  "isValid": true/false,
  "issues": [
    {
      "level": "error" | "warning" | "info",
      "section": "название раздела или \\"Структура\\" | \\"Кодировка\\" | \\"Форматирование\\"",
      "issue": "конкретное описание проблемы (что найдено, где находится)",
      "suggestion": "конкретное предложение по исправлению"
    }
  ],
  "summary": "краткий вывод (1-2 предложения): сколько ошибок, готов ли документ"
}

ВАЖНО: JSON должен быть ПОСЛЕДНИМ в ответе. После JSON ничего не пиши.

ПРАВИЛА ФОРМИРОВАНИЯ ОТВЕТА:
1. isValid = true ТОЛЬКО если нет ошибок уровня "error"
2. issues должны содержать ТОЛЬКО реальные найденные проблемы (не выдуманные)
3. Каждый issue должен быть конкретным (не "есть ошибки", а "в разделе 3 отсутствует должность у участника Иванов")
4. suggestion должен быть практичным (не "исправьте ошибку", а "добавьте должность после ФИО")
5. НЕ дублировать проблемы (если одна ошибка в нескольких местах — перечислить все места)
6. summary должен отражать реальное состояние (не "документ хороший", а "найдено 3 ошибки, требуется исправление")
7. КРИТИЧЕСКИ ВАЖНО: Не создавай ложных ошибок — проверяй каждый пункт

================================================================================
## ПРИМЕРЫ ПРАВИЛЬНЫХ ОТВЕТОВ:
================================================================================

ПРИМЕР 1 (реальный китайский символ):
{
  "isValid": false,
  "issues": [
    {
      "level": "error",
      "section": "Кодировка",
      "issue": "Найден недопустимый символ '你' (Unicode: U+4F60) в разделе 6",
      "suggestion": "Удалить недопустимый символ или заменить на корректный символ кириллицы"
    }
  ],
  "summary": "Найдена 1 критическая ошибка (недопустимый символ). Документ требует исправления."
}

ПРИМЕР 2 (действительно отсутствует раздел):
{
  "isValid": false,
  "issues": [
    {
      "level": "error",
      "section": "Структура",
      "issue": "Отсутствует раздел 5 'Сокращения и обозначения' — в документе есть разделы 1-4 и 6-10, но раздел 5 пропущен",
      "suggestion": "Добавить раздел 5 со списком сокращений или указать 'Сокращения не используются'"
    }
  ],
  "summary": "Отсутствует 1 обязательный раздел. Документ требует исправления."
}

ПРИМЕР 3 (неполное ФИО):
{
  "isValid": false,
  "issues": [
    {
      "level": "error",
      "section": "Участники",
      "issue": "Участник 'Екатерина' указан только по имени без фамилии — в деловом протоколе должно быть полное ФИО",
      "suggestion": "Указать полную фамилию и имя участника, например: 'Екатерина Иванова' или 'Е. Иванова'"
    }
  ],
  "summary": "Найдена 1 критическая ошибка (неполное ФИО участника). Документ требует исправления."
}

ПРИМЕР 4 (неделовой стиль):
{
  "isValid": false,
  "issues": [
    {
      "level": "error",
      "section": "Термины и определения",
      "issue": "В определении термина 'Старший системный аналитик' найдено слово не делового стиля 'Мяу'",
      "suggestion": "Удалить слово 'Мяу' из определения. Деловой документ должен содержать только профессиональную лексику"
    }
  ],
  "summary": "Найдена 1 критическая ошибка (неделовой стиль). Документ требует исправления."
}

ПРИМЕР 5 (ошибок нет):
{
  "isValid": true,
  "issues": [],
  "summary": "Ошибок не найдено. Документ соответствует требованиям и готов к использованию."
}

================================================================================
## НАЧИНАЙ ПРОВЕРКУ ДОКУМЕНТА. ОТВЕЧАЙТЕ ТОЛЬКО JSON.
================================================================================`;

  try {
    const openrouter = createOpenRouter({
      apiKey: process.env.OPENROUTER_API_KEY!,
      baseURL: 'https://openrouter.ai/api/v1',
      compatibility: 'strict',
    });

    const model = openrouter.chat('arcee-ai/trinity-large-preview:free');

    console.log('[review-agent] === НАЧАЛО ПРОВЕРКИ ДОКУМЕНТА ===');
    console.log('[review-agent] Длина документа:', normalizedDoc.length, 'символов');
    const startTime = Date.now();

    const response = await generateText({
      model,
      prompt: reviewPrompt,
      temperature: 0.0, // Нулевая температура для максимальной детерминированности
      seed: 42, // Фиксированный seed для воспроизводимости
    });

    const elapsedTime = Date.now() - startTime;
    console.log(`[review-agent] Генерация завершена за ${elapsedTime}ms`);
    console.log('[review-agent] Длина сырого ответа:', response.text.length, 'символов');

    // Извлекаем JSON (всё между первой { и последней })
    const jsonMatch = response.text.match(/\{[\s\S]*\}/);
    if (!jsonMatch) {
      console.error('[review-agent] ❌ JSON не найден в ответе');
      console.log('[review-agent] Полный ответ (первые 2000 символов):');
      console.log(response.text.slice(0, 2000));
      throw new Error('Не удалось распарсить ответ агента проверки');
    }

    // Извлекаем reasoning (всё до JSON) для логирования
    const reasoningText = response.text.substring(0, jsonMatch.index).trim();
    console.log('[review-agent] === REASONING МОДЕЛИ ===');
    console.log(reasoningText);
    console.log('[review-agent] === КОНЕЦ REASONING ===');

    const parsed: DocumentReview = JSON.parse(jsonMatch[0]);
    
    // Валидация структуры ответа
    if (typeof parsed.isValid !== 'boolean') {
      console.warn('[review-agent] ⚠️ Неверное поле isValid, установлено false');
      parsed.isValid = false;
    }
    if (!Array.isArray(parsed.issues)) {
      console.warn('[review-agent] ⚠️ Неверное поле issues, установлен пустой массив');
      parsed.issues = [];
    }
    if (!parsed.summary || typeof parsed.summary !== 'string') {
      console.warn('[review-agent] ⚠️ Неверное поле summary, установлено значение по умолчанию');
      parsed.summary = 'Ошибка формирования ответа проверки.';
    }
    
    // Подсчёт статистики
    const errorCount = parsed.issues.filter(i => i.level === 'error').length;
    const warningCount = parsed.issues.filter(i => i.level === 'warning').length;
    const infoCount = parsed.issues.filter(i => i.level === 'info').length;
    
    console.log('[review-agent] === РЕЗУЛЬТАТЫ ПРОВЕРКИ ===');
    console.log(`[review-agent] isValid: ${parsed.isValid}`);
    console.log(`[review-agent] Найдено ошибок: ${errorCount} (error), ${warningCount} (warning), ${infoCount} (info)`);
    console.log(`[review-agent] Summary: ${parsed.summary}`);
    
    if (parsed.issues.length > 0) {
      console.log('[review-agent] === СПИСОК ОШИБОК ===');
      parsed.issues.forEach((issue, idx) => {
        console.log(`[review-agent] [${idx + 1}] ${issue.level.toUpperCase()} | ${issue.section}`);
        console.log(`[review-agent]     Проблема: ${issue.issue}`);
        if (issue.suggestion) {
          console.log(`[review-agent]     Решение: ${issue.suggestion}`);
        }
      });
      console.log('[review-agent] === КОНЕЦ СПИСКА ОШИБОК ===');
    } else {
      console.log('[review-agent] ✓ Ошибок не найдено');
    }
    console.log('[review-agent] === ПРОВЕРКА ЗАВЕРШЕНА ===');
    
    return parsed;
  } catch (error) {
    console.error('[review-agent] ❌ КРИТИЧЕСКАЯ ОШИБКА:', error);
    throw new Error(`Ошибка при проверке документа: ${String(error)}`);
  }
}
